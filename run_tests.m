function run_tests()

env_size = 4;
step_count = 100;
plot_control = 0;
quick_stop = false;
img_output = true;

agents = [360 0 40; 260 100 40; 160 200 40; 60 300 40; 10 350 40];

disease = [5 10; 4 8; 3 6];

for i = 1:size(agents)
    vuln_count = agents(i, 1);
    vacc_count = agents(i, 2);
    infec_count = agents(i, 3);
    
    for j = 1:size(disease)
        curability = disease(j, 1);
        lethality = disease(j, 2);
        
        fprintf(...
            '\n\nherdlab(%i, %03i, %03i, %i, %i, %i, %02i, %i, %i, %i);\n',...
            env_size,...
            vuln_count,...
            vacc_count,...
            infec_count,...
            step_count,...
            curability,...
            lethality,...
            plot_control,...
            quick_stop,...
            img_output...
            );
        
        herdlab(env_size,...
            vuln_count,...
            vacc_count,...
            infec_count,...
            step_count,...
            curability,...
            lethality,...
            plot_control,...
            quick_stop,...
            img_output...
            );
    end
end
end

